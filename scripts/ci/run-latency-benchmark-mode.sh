#!/usr/bin/env bash
set -euo pipefail

MODE="${1:-strict-ci}"
COMPOSE_FILE="${2:-docker/shard-with-autopropose.yml}"
DURATION_SECONDS="${3:-120}"
OUT_DIR="${4:-/tmp/casper-latency-benchmark-${MODE}-$(date -u +%Y%m%dT%H%M%SZ)}"

case "$MODE" in
  strict-ci)
    export AUTO_RECREATE_ON_PRELOAD_FAIL="${AUTO_RECREATE_ON_PRELOAD_FAIL:-0}"
    export AUTO_RECREATE_MAX_ATTEMPTS="${AUTO_RECREATE_MAX_ATTEMPTS:-1}"
    export PRELOAD_REQUIRE_PEERS_MIN="${PRELOAD_REQUIRE_PEERS_MIN:-3}"
    export PRELOAD_RETRY_RATIO_MAX="${PRELOAD_RETRY_RATIO_MAX:-2.50}"
    export PRELOAD_RETRY_RATIO_MIN_REQUESTS="${PRELOAD_RETRY_RATIO_MIN_REQUESTS:-100}"
    export POSTLOAD_RETRY_RATIO_MAX="${POSTLOAD_RETRY_RATIO_MAX:-2.50}"
    export POSTLOAD_RETRY_RATIO_MIN_REQUESTS="${POSTLOAD_RETRY_RATIO_MIN_REQUESTS:-100}"
    ;;
  soak-autoheal)
    export AUTO_RECREATE_ON_PRELOAD_FAIL="${AUTO_RECREATE_ON_PRELOAD_FAIL:-1}"
    export AUTO_RECREATE_MAX_ATTEMPTS="${AUTO_RECREATE_MAX_ATTEMPTS:-1}"
    export PRELOAD_REQUIRE_PEERS_MIN="${PRELOAD_REQUIRE_PEERS_MIN:-3}"
    export PRELOAD_RETRY_RATIO_MAX="${PRELOAD_RETRY_RATIO_MAX:-2.50}"
    export PRELOAD_RETRY_RATIO_MIN_REQUESTS="${PRELOAD_RETRY_RATIO_MIN_REQUESTS:-100}"
    export POSTLOAD_RETRY_RATIO_MAX="${POSTLOAD_RETRY_RATIO_MAX:-}"
    export POSTLOAD_RETRY_RATIO_MIN_REQUESTS="${POSTLOAD_RETRY_RATIO_MIN_REQUESTS:-100}"
    ;;
  *)
    cat >&2 <<EOF
Unknown mode: $MODE
Usage: $0 [strict-ci|soak-autoheal] [compose_file] [duration_seconds] [out_dir]
EOF
    exit 2
    ;;
esac

echo "Latency benchmark mode: $MODE"
echo "  compose_file: $COMPOSE_FILE"
echo "  duration_seconds: $DURATION_SECONDS"
echo "  output_dir: $OUT_DIR"
echo "  AUTO_RECREATE_ON_PRELOAD_FAIL=$AUTO_RECREATE_ON_PRELOAD_FAIL"
echo "  AUTO_RECREATE_MAX_ATTEMPTS=$AUTO_RECREATE_MAX_ATTEMPTS"
echo "  PRELOAD_REQUIRE_PEERS_MIN=$PRELOAD_REQUIRE_PEERS_MIN"
echo "  PRELOAD_RETRY_RATIO_MAX=$PRELOAD_RETRY_RATIO_MAX"
echo "  PRELOAD_RETRY_RATIO_MIN_REQUESTS=$PRELOAD_RETRY_RATIO_MIN_REQUESTS"
echo "  POSTLOAD_RETRY_RATIO_MAX=${POSTLOAD_RETRY_RATIO_MAX:-<disabled>}"
echo "  POSTLOAD_RETRY_RATIO_MIN_REQUESTS=$POSTLOAD_RETRY_RATIO_MIN_REQUESTS"

exec ./scripts/ci/run-latency-benchmark.sh "$COMPOSE_FILE" "$DURATION_SECONDS" "$OUT_DIR"
